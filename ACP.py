# Symmetric Difference Example A
set1_A = {'blue', 'green'}
set2_A = {'blue', 'yellow'}

result_A = set1_A.symmetric_difference(set2_A)

print("Symmetric Difference (A):", result_A)


# Symmetric Difference Example B
set1_B = {1, 2, 3, 4, 5}
set2_B = {1, 5, 6, 7, 8, 9}

result_B = set1_B.symmetric_difference(set2_B)

print("Symmetric Difference (B):", result_B)